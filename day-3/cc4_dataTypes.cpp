/*
Declare an int named studentID and assign it a value
Declare a float (or double) named score and assign it a value (use a decimal)
Declare a char named grade and assign it a single character (in single quotes)
Print all three values on new lines, using cout
*/


#include <iostream>
using namespace std;

int main(){
	int studentID = 19;
	double score = 4.0;
	char grade = 'A';
	cout << "student ID : " << studentID << "\n";
	cout << "score : " << score << "\n";
	cout << "grade : " << grade << "\n";
	return 0;
}	
